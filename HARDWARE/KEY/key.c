#include "key.h"
#include "delay.h"

void KEY_Init(void)
{
	
	GPIO_InitTypeDef GPIO_InitStructure;

 	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA|RCC_APB2Periph_GPIOB|RCC_APB2Periph_AFIO,ENABLE);//使能PORTA,PORTC时钟

			GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;			
			GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 		 //推挽输出
			GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;		 //IO口速度为50MHz
			GPIO_Init(GPIOA, &GPIO_InitStructure);				
			GPIO_SetBits(GPIOA,GPIO_Pin_10);	

			GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12;			
			GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 		 //推挽输出
			GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;		 //IO口速度为50MHz
			GPIO_Init(GPIOB, &GPIO_InitStructure);				
			GPIO_SetBits(GPIOB,GPIO_Pin_12);	
	
			GPIO_InitStructure.GPIO_Pin  = GPIO_Pin_5;
			GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD; //设置成上拉输入
			GPIO_Init(GPIOB, &GPIO_InitStructure);
			
			GPIO_InitStructure.GPIO_Pin  = GPIO_Pin_6;
			GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD; //设置成上拉输入
			GPIO_Init(GPIOB, &GPIO_InitStructure);
		 
			GPIO_InitStructure.GPIO_Pin  = GPIO_Pin_7;
			GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD; 
			GPIO_Init(GPIOB, &GPIO_InitStructure);
} 

//按键处理函数
//返回按键值
//mode:0,不支持连续按;1,支持连续按;
//返回值：
//0，没有任何按键按下
//KEY0_PRES，KEY0按下
//KEY1_PRES，KEY1按下
//WKUP_PRES，WK_UP按下 
//注意此函数有响应优先级,KEY0>KEY1>WK_UP!!
u8 KEY_Scan(u8 mode)
{	 
	static u8 key_up=1;//按键按松开标志
	if(mode)key_up=1;  //支持连按		  
	if(key_up&&(KEY0==1||KEY1==1||WK_UP==1))
	{
		delay_ms(10);//去抖动 
		key_up=0;
		if(KEY0==1)return KEY0_PRES;
		else if(KEY1==1)return KEY1_PRES;
		else if(WK_UP==1)return WKUP_PRES; 
	}else if(KEY0==0&&KEY1==0&&WK_UP==0)key_up=1; 	     
	return 0;// 无按键按下
}

